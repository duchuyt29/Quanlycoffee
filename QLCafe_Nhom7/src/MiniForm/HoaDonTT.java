    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        // TODO add your handling code here:
        try {
            double tt = HanhDong.TongTien - HanhDong.TongTien * SuKien.getUuDai() / 100;
            HDDAO.thanhToanHoaDon(ShareHelper.User.getTenTaiKhoan(), HanhDong.TongTien,
                    SuKien.getMaSuKien(), SuKien.getUuDai(), tt, MaHD);
            BHDao.updateTrangThaiNgoiTheoMa(0, Ban.getMaBan());
            JOptionPane.showMessageDialog(this, "Thanh toán thành công");
            
            hanhDongBanHang.LoadBanToPanel();
            HanhDong.MaHD = null;
            hanhDongBanHang.loadDaTaToTableCTHoaDon();
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi thanh toán");
            e.printStackTrace();

        }
    }//GEN-LAST:event_btnXacNhanActionPerformed

